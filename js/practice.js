    this.items = this.items.filter((item) => item.text !== text);//–≤ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤ –∑–∞–ø–∏—Å–∞—Ç—å —Ç–µ–∫—Å—Ç –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç, –º—ã –µ–≥–æ –æ—Å—Ç–∞–≤–ª—è–µ–º
